# 吉祥数字 100 ~ 999     111 222 333 444 555....   123 234 345 456 567....789    987 876 765 ....321 210
i = 100
while i < 1000:
    # 获取三位一样的数字 111 222 333   1000 9999
    if i % 111 == 0:
        print(i)

    # 获取连续递增的数字
    # 123%100 =23 //10 =2       456//10=45%10 5       234%10 23......4
    bai = i // 100
    shi = i // 10 % 10
    ge = i % 10

    if bai + 1 == shi and shi + 1 == ge:
        print(i)

    if bai - 1 == shi and shi - 1 == ge:
        print(i)

    i += 1
